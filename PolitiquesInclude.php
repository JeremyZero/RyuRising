ARTICLE 1 – RENSEIGNEMENTS PERSONNELS RECUEILLIS<br>

<p>Lorsque tu effectue un achat sur notre boutique, dans le cadre de notre processus d’achat et de vente, nous recueillons les renseignements personnels que tu nous fourni, tels que ton nom, ton adresse et ton adresse e-mail.<br>

Lorsque tu navigue sur notre boutique, nous recevons également automatiquement l’adresse de protocole Internet (adresse IP) de ton ordinateur, qui nous permet d’obtenir plus de détails au sujet du navigateur et du système d’exploitation que tu utilises.<br>

Marketing par e-mail (le cas échéant): Avec ta permission, nous pourrions t'envoyer des e-mails au sujet de notre boutique, de nouveaux produits et d’autres mises à jour.</p><br>


ARTICLE 2 - CONSENTEMENT<br>

<p>Comment obtenez-vous mon consentement?<br>

Lorsque tu nous fourni tes renseignements personnels pour conclure une transaction, vérifier ta carte de crédit, passer une commande, planifier une livraison ou retourner un achat, nous présumons que tu consens à ce que nous recueillions tes renseignements et à ce que nous les utilisions à cette fin uniquement.<br>

Si nous te demandons de nous fournir tes renseignements personnels pour une autre raison, à des fins de marketing par exemple, nous te demanderons directement ton consentement explicite, ou nous te donnerons la possibilité de refuser.</p><br>


<p>Comment puis-je retirer mon consentement?<br>

Si après nous avoir donné ton consentement, tu changes d’avis et ne consens plus à ce que nous puissions te contacter, recueillir tes renseignements ou les divulguer, tu peux nous en aviser en nous contactant à info@ryurising.com ou par courrier à: RYU RISING SAS 63 Avenue Simone Veil 06200 Nice, France.</p><br>


ARTICLE 3 – DIVULGATION<br>

<p>Nous pouvons divulguer tes renseignements personnels si la loi nous oblige à le faire ou si tu violes nos Conditions Générales de Vente et d’Utilisation.</p><br>


ARTICLE 4 – SHOPIFY<br>

<p>Notre boutique est hébergée sur Shopify Inc. Ils nous fournissent la plate-forme e-commerce en ligne qui nous permet de te vendre nos services et produits.<br>

Tes données sont stockées dans le système de stockage de données et les bases de données de Shopify, et dans l’application générale de Shopify. Tes données sont conservées sur un serveur sécurisé protégé par un pare-feu.</p><br>


Paiement:<br>

<p>Si tu réalises ton achat par le biais d’une passerelle de paiement direct, dans ce cas Shopify stockera tes renseignements de carte de crédit. Ces renseignements sont chiffrés conformément à la norme de sécurité des données établie par l’industrie des cartes de paiement (norme PCI-DSS). Les renseignements relatifs à ta transaction d’achat sont conservés aussi longtemps que nécessaire pour finaliser ta commande. Une fois ta commande finalisée, les renseignements relatifs à la transaction d’achat sont supprimés.<br>

Toutes les passerelles de paiement direct respectent la norme PCI-DSS, gérée par le conseil des normes de sécurité PCI, qui résulte de l’effort conjoint d’entreprises telles que Visa, MasterCard, American Express et Discover.<br>

Les exigences de la norme PCI-DSS permettent d’assurer le traitement sécurisé des données de cartes de crédit par notre boutique et par ses prestataires de services.</p><br>

Pour plus d’informations, veuillez consulter les Conditions d’Utilisation de Shopify.<br>


ARTICLE 5 – SERVICES FOURNIS PAR DES TIERS<br>


<p>De manière générale, les fournisseurs tiers que nous utilisons vont uniquement recueillir, utiliser et divulguer tes renseignements dans la mesure du nécessaire pour pouvoir réaliser les services qu’ils nous fournissent.<br>

Cependant, certains tiers fournisseurs de services, comme les passerelles de paiement et autres processeurs de transactions de paiement, possèdent leurs propres politiques de confidentialité quant aux renseignements que nous sommes tenus de leur fournir pour tes transactions d’achat.<br>

En ce qui concerne ces fournisseurs, nous te recommandons de lire attentivement leurs politiques de confidentialité pour que tu puisses comprendre la manière dont ils traiteront tes renseignements personnels.<br>

Il ne faut pas oublier que certains fournisseurs peuvent être situés ou avoir des installations situées dans une juridiction différente de la tienne ou de la nôtre. Donc si tu décides de poursuivre une transaction qui requiert les services d’un fournisseur tiers, tes renseignements pourraient alors être régis par les lois de la juridiction dans laquelle ce fournisseur se situe ou celles de la juridiction dans laquelle ses installations sont situées.<br>

À titre d’exemple, si tu es situé au Canada et que ta transaction est traitée par une passerelle de paiement située aux États-Unis, les renseignements  t'appartenant qui ont été utilisés pour conclure la transaction pourraient être divulgués en vertu de la législation des États-Unis, y compris le Patriot Act.<br>

Une fois que tu quittes le site de notre boutique ou que tu es redirigé vers le site web ou l’application d’un tiers, tu n’es plus régi par la présente Politique de Confidentialité ni par les Conditions Générales de Vente et d’Utilisation de notre site web.</p><br>


Liens<br>

