ARTICLE 6 – SÉCURITÉ<br>

<p>Pour protéger tes données personnelles, nous prenons des précautions raisonnables et suivons les meilleures pratiques de l’industrie pour nous assurer qu’elles ne soient pas perdues, détournées, consultées, divulguées, modifiées ou détruites de manière inappropriée.<br>

Si tu nous fourni tes informations de carte de crédit, elles seront chiffrées par le biais de l’utilisation du protocole de sécurisation SSL et conservées avec un chiffrement de type AES-256. Bien qu’aucune méthode de transmission sur Internet ou de stockage électronique ne soit sûre à 100 %, nous suivons toutes les exigences de la norme PCI-DSS et mettons en œuvre des normes supplémentaires généralement reconnues par l’industrie.<br>
FICHIERS TÉMOINS (COOKIES)</p><br>

<p>Voici une liste de fichiers témoins que nous utilisons. Nous les avons énumérés ici pour que tu aies la possibilité de choisir si tu souhaites les autoriser ou non.<br>

_session_id, identificateur unique de session, permet à Shopify de stocker les informations relatives à ta session (référent, page de renvoi, etc.).<br>

_shopify_visit, aucune donnée retenue, persiste pendant 30 minutes depuis la dernière visite. Utilisé par le système interne de suivi des statistiques du fournisseur de notre site web pour enregistrer le nombre de visites.<br>

_shopify_uniq, aucune donnée retenue, expire à minuit (selon l’emplacement du visiteur) le jour suivant. Calcule le nombre de visites d’une boutique par client unique.<br>

cart, identificateur unique, persiste pendant 2 semaines, stocke l’information relative à votre panier d’achat.<br>

_secure_session_id, identificateur unique de session<br>

storefront_digest, identificateur unique, indéfini si la boutique possède un mot de passe, il est utilisé pour savoir si le visiteur actuel a accès.</p><br>


